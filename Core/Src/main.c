void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	UNUSED(htim);

	mpu6050_update(&mpu6050);
	mpu6050_calc_pitch(&mpu6050, &pitch);


	error = setpoint - pitch;

	P=Kp * error;

	I+= (Ki * error);
	if (I >maxPWM)
		I =  maxPWM;
	else if (I < minPWM)
		I=minPWM;

	D=-Kd * (error - lastError);

	float PID_PWM = P+I+D;

	uint8_t out_pwm;
	if (PID_PWM > maxPWM)
		out_pwm=maxPWM;
	else if (PID_PWM < minPWM)
		out_pwm= minPWM;
	else
		out_pwm= (uint8_t) PID_PWM;
	lastError = error;

	HAL_GPIO_WritePin(IN1_GPIO_Port,IN1_Pin,
			error > 0.0 ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(IN2_GPIO_Port, IN2_Pin,
			error > 0.0 ? GPIO_PIN_RESET : GPIO_PIN_SET);
	HAL_GPIO_WritePin(IN3_GPIO_Port, IN3_Pin,
			error > 0.0 ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(IN4_GPIO_Port, IN4_Pin,
			error > 0.0 ? GPIO_PIN_RESET : GPIO_PIN_SET);
	htim1.Instance-> CCR1 = out_pwm;
	htim1.Instance-> CCR2 = out_pwm;
}
